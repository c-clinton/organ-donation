    <h1>FAQ</h1>

    <div class="small-12 columns">

    <p>If you have a specific question to ask, <a href="contact.php" class="green">let us know</a>.</p>

<details>
<summary div class="arrow">
<div class="row">
<h2 class="small-8 column faq1">What organs and tissue can be donated?</h2>
  </div>
</summary>
    <p class="columns faqInfo1">Organs and tissue that can be donated include the heart, kidneys, liver, lungs, pancreas, small intestines, eyes, bone, skin, and heart valves.</p>
 </details>

    <details>
    <summary div class="arrow">
   <div class="row">
    <h2 class="small-8 columns faq2">Does my age, medical condition, or sexual orientation prevent me from being a donor?</h2>
  </div>
  </summary>
    <p class="columns faqInfo2">Everyone is a potential donor regardless of age, medical condition or sexual orientation. The oldest Canadian organ donor was 92 and the oldest tissue donor was 104. Even individuals with serious illnesses can sometimes be donors. Your decision to register should not be based on whether you think you would be eligible or not. All potential donors are evaluated on an individual, medical, case-by-case basis. </p>
    </details>

    <details>
    <summary div class="arrow">
     <div class="row">
    <h2 class="small-8 columns faq3">Why should I register as an organ and tissue donor?</h2>
  </div>
  </summary>
    <p class="columns faqInfo3">By registering consent for organ and tissue donation, you give hope to the thousands of Ontarians waiting for a transplant. Individuals on the transplant wait list are suffering from organ failure and without the generous gift of life from an organ donor, they will die. Tissue donors can also enhance the lives of recovering burn victims, help restore sight, and allow people to walk again. Transplants not only save lives, they return recipients to productive lives.</p>
    </details>

    <details>
    <summary div class="arrow">
   <div class="row">
    <h2 class="small-8 columns faq4">Does my religion support organ and tissue donation?</h2>
  </div>
  </summary>
    <p class="columns faqInfo4">Most major religions support organ and tissue donation because it can save the life of another. If your religion restricts the use of a body after death, consult your religious leader: these restrictions may not include organ and tissue donation, if the donation could save another life. More information can be found <a href="http://www.giftoflife.on.ca/en/faq.htm" target="_blank" class="green">here</a>.</p>
    </details>

	<details>
    <summary div class="arrow">
    <div class="row">
    <h2 class="small-8 columns faq5">What does it mean to consent to donate organs and tissue for research?</h2>
  </div>
  </summary>
    <p class="columns faqInfo5">Organs or tissue not suitable for transplantation can be used for organ and tissue research (if indicated by donor upon registration). This research is specific to the field of organ and tissue donation, and is not the same as whole body donation.</p>
    </details>

	<details>
    <summary div class="arrow">
    <div class="row">
    <h2 class="small-8 columns faq6">I signed my donor card, do I need to register again?</h2>
  </div>
  </summary>
    <p class="columns faqInfo6">Paper donor cards are no longer in use as they often were not available when needed. In 2008, Trillium Gift of Life Network adopted an affirmative registry and now your consent to donate organs and tissue is stored in a Ministry of Health and Long-Term Care database. By formally registering, either online at <a href="index.php" class="green">www.BeADonor.ca</a> or in person at any ServiceOntario location, you ensure that your decision is recorded and can be made available to your loved ones at the right time. You only need to register once.</p>
    </details>

	<details>
    <summary class="arrow">
    <div class="row">
    <h2 class="small-8 columns faq7">How can I check to see if I've already registered as a donor?</h2>
  </div>
  </summary>
  <div class="columns faqInfo7">
    <p >There are two ways to check if you are already registered as an organ and tissue donor:
</p>
</div>
      <ol class="columns faqInfo7b">
    <li>On the <a class="green" href="index.php">www.BeADonor.ca</a> home page choose “Check Now.” This will take you to the ServiceOntario online registration page. Click on “Register, check or update your consent online.” The system will ask for identification. Enter in that information, and click on “Check or Update Registration.” If your registration has been processed, the system will respond, “Yes, you are a registered organ and tissue donor.” (If not, the system will then ask you if you wish to register.)</li>
    <li>You can also check the back of your photo health card. If the word “Donor” is present, you are registered and do not need to register again.</li>
  </ol>
   </details>


	<details>
    <summary div class="arrow">
    <div class="row">
    <h2 class="small-8 columns faq8">I'm not able to register or check online.</h2>
  </div>
  </summary>
    <p class="columns faqInfo8">If the system is not able to register you online, it may ask you to contact ServiceOntario directly. There are a number of reasons that might prevent you from registering online. Visit any ServiceOntario location to register in person.</p>
    </details>

	<details>
    <summary div class="arrow">
    <div class="row">
    <h2 class="small-8 columns faq9">How do I update or withdraw my consent to organ and tissue donation?</h2>
  </div>
  </summary>
    <p class="columns faqInfo9">

You can update or withdraw your consent at any time at www.BeADonor.ca by simply choosing “Check or Update Registration” on the home page. You can also visit any ServiceOntario centre to update or withdraw in person. You can also withdraw your consent by mailing a letter to the below address with your name, date of birth, health card number and mailing address.</p>


<p class="columns faqInfo9b">Organ Donor Consent
ServiceOntario
P.O. Box 48
Kingston, ON
K7L 5J3
</p>

</details>

	<details>
    <summary div class="arrow">
    <div class="row">
    <h2 class="small-8 columns faq10">How do I register if I live in Ontario, but don’t have an Ontario health card?</h2>
  </div>
  </summary>
    <p class="columns faqInfo10">In order to register consent in Ontario, you must have a valid Ontario health card. This is because the registered consent will be stored in a Ministry of Health and Long Term Care database. Without an Ontario health card, you will not be able to formally register. However, it is important to talk to your family about your wishes. When someone passes away and donation is possible, an Organ and Tissue Donation Coordinator will approach families to talk about donation. By talking to your family about your wishes, you can help relieve the burden of making that choice without confirmation of your wishes. </p>
    </details>

	<details>
    <summary div class="arrow">
    <div class="row">
    <h2 class="small-8 columns faq11">What is involved in the organ donation process and how long will donation take?</h2>
  </div>
  </summary>
    <p class="columns faqInfo11">When an Organ and Tissue Donation Coordinator is preparing to speak with a family about donation, they will access the Ministry of Health and Long Term Care database to see if their loved one was a registered donor. That information would be shared with the family and they would be asked to reaffirm that choice. Once consent is given, medical tests are completed to determine what organs and tissues are suitable for transplant. The organs are then matched with someone on the transplant wait list and surgery takes place in an operating room at the hospital. The entire donation process, from the time the family agrees to move forward with donation to recovery, takes about 24 to 36 hours to complete. </p>
    </details>

	<details>
    <summary div class="arrow">
    <div class="row">
    <h2 class="small-8 columns faq12">Can my family overrule my decision to donate?</h2>
  </div>
  </summary>
    <p class="columns faqInfo12">

When you register your consent to donate, this information is recorded and stored in a Ministry of Health and Long-Term Care database. Your decision will only be accessed should there be potential for donation, and your status as a registered donor will be shared with your family. It is Trillium Gift of Life Network's practice to reaffirm an individual's consent to donate with the family. In most cases, families honour their loved ones' decision to donate if they have evidence that it's what they wanted.</p>

<p class="columns faqInfo12b">
Register as an organ donor and talk to your family about your wishes; one day this act could save a life.
</p>
	</details>


	<details>
    <summary div class="arrow">
    <div class="row">
    <h2 class="small-8 columns faq13">How do you know if my organs and tissue are suitable to donate?</h2>
  </div>
  </summary>
    <p class="columns faqInfo13">Eligibility to donate is assessed at the time of death from information obtained during a medical social questionnaire completed with families. In addition, every organ is tested for suitability to ensure that as many people as possible can be helped through transplant.</p>
    </details>

	<details>
    <summary div class="arrow">
    <div class="row">
    <h2 class="columns faq14">Do families pay any costs associated with organ and/or tissue donation?</h2>
  </div>
  </summary>
    <p class="columns faqInfo14">There are no costs to the donor’s family for organ and tissue donation. However, expenses related to funeral arrangements remain the responsibility of the donor’s family.</p>
    </details>

	<details>
    <summary div class="arrow">
    <div class="row">
    <h2 class="small-8 columns faq15">What happens after the organs and tissue are removed?</h2>
  </div>
  </summary>
    <p class="columns faqInfo15">The body is released to the family, and funeral arrangements can continue as planned.</p>
    </details>

	<details>
    <summary div class="arrow">
    <div class="row">
    <h2 class="small-8 columns faq16">What impact does organ donation have on funeral plans? Can I have an open casket at the funeral?</h2>
  </div>
  </summary>
  <div class="columns faqInfo16">
    <p>
Organ and tissue donation does not impact funeral plans. Cremation and an open casket funeral are both possible. </p>

    <ul class="columns faqInfo16b">
    <li>Organ recovery from the abdomen or chest usually involves one surgical incision that clothing would cover.</li>
    <li>When corneas are donated, typically the whole globe of the eye is removed. Funeral homes provide eye caps to maintain the shape and form of the eyes. This is usual practice whether eyes are donated or not.</li>
    <li>The appearance of the skin after recovery is similar to that of sunburn. A paper-thin layer of skin is removed from the back of the body permitting an open casket funeral.</li>
  </ul>

</div>
</details>







